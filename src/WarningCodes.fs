    | W_CURLY_INDEXING_NON_CELL     -> "W_CURLY_INDEXING_NON_CELL"
    | W_CELL_ASSIGN_NON_CELL        -> "W_CELL_ASSIGN_NON_CELL"
    | W_RETURN_OUTSIDE_FUNCTION     -> "W_RETURN_OUTSIDE_FUNCTION"
    | W_BREAK_OUTSIDE_LOOP          -> "W_BREAK_OUTSIDE_LOOP"
    | W_CONTINUE_OUTSIDE_LOOP       -> "W_CONTINUE_OUTSIDE_LOOP"
