    | W_TRANSPOSE_TYPE_MISMATCH     -> "W_TRANSPOSE_TYPE_MISMATCH"
    | W_NEGATE_TYPE_MISMATCH        -> "W_NEGATE_TYPE_MISMATCH"
    | W_NOT_TYPE_MISMATCH           -> "W_NOT_TYPE_MISMATCH"
    | W_STRING_ARITHMETIC           -> "W_STRING_ARITHMETIC"
    | W_INDEXING_SCALAR             -> "W_INDEXING_SCALAR"
